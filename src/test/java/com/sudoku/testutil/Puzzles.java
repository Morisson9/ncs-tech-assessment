package com.sudoku.testutil;

import com.sudoku.model.Position;

/**
 * Canonical puzzle data used across the tests. The givens and solution are taken
 * from the example in the problem statement, which gives us a known grid with
 * exactly 30 givens to assert against.
 */
public final class Puzzles {

    private Puzzles() {
    }

    public static int[][] canonicalGivens() {
        return new int[][]{
                {5, 3, 0, 0, 7, 0, 0, 0, 0},
                {6, 0, 0, 1, 9, 5, 0, 0, 0},
                {0, 9, 8, 0, 0, 0, 0, 6, 0},
                {8, 0, 0, 0, 6, 0, 0, 0, 3},
                {4, 0, 0, 8, 0, 3, 0, 0, 1},
                {7, 0, 0, 0, 2, 0, 0, 0, 6},
                {0, 6, 0, 0, 0, 0, 2, 8, 0},
                {0, 0, 0, 4, 1, 9, 0, 0, 5},
                {0, 0, 0, 0, 8, 0, 0, 7, 9}
        };
    }

    public static int[][] canonicalSolution() {
        return new int[][]{
                {5, 3, 4, 6, 7, 8, 9, 1, 2},
                {6, 7, 2, 1, 9, 5, 3, 4, 8},
                {1, 9, 8, 3, 4, 2, 5, 6, 7},
                {8, 5, 9, 7, 6, 1, 4, 2, 3},
                {4, 2, 6, 8, 5, 3, 7, 9, 1},
                {7, 1, 3, 9, 2, 4, 8, 5, 6},
                {9, 6, 1, 5, 3, 7, 2, 8, 4},
                {2, 8, 7, 4, 1, 9, 6, 3, 5},
                {3, 4, 5, 2, 8, 6, 1, 7, 9}
        };
    }

    /** The full solution with the named cells blanked out, for building near-complete puzzles. */
    public static int[][] solutionWithBlanks(Position... blanks) {
        int[][] grid = canonicalSolution();
        for (Position blank : blanks) {
            grid[blank.row()][blank.col()] = 0;
        }
        return grid;
    }
}
